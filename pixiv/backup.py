def get_ip_list():
    ip_list = []
    html = requests.get("http://haoip.cc/tiqu.htm")
    all_ip = re.findall(r'r/>(.*?)<b', html.text, re.S)
    # 表示从html.text中获取所有r/><b中的内容，re.S的意思是包括匹配包括换行符，findall返回list
    for ip in all_ip:
        tmp_ip = re.sub('\n', '', ip)
        ip_list.append(tmp_ip.strip())
    return ip_list

